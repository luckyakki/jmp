package com.luckymart.jmp;

public class Incrementoperator {              // increment operater ++ ,  --   //

	public static void main(String[] args) {
		
		int a = 10;
		System.out.println(a++);
		
	
		int b = 10;
		System.out.println(++b);  
		
		int c = 10;
		System.out.println(c--);
		                            
		 int d = 10;                                                     // assignment oper 
	     System.out.println(--d);	                                                   //  c -= 2 --> c=c-2
				                                                   //  c *= 2 --> c=c*2
				                                                    // c /= 2
				                                                    // c %= 2
				                                                    // c <<= 2 
				                                                    // c >>= 2 
				                                                   //  c >>= 2 
				                                                    // c &= 2
				                                                    // c ^= 2
				                                                   //  c |= 2  
		
				

	}
}


